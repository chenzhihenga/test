TaskHandle_t starttask_handler;
void start_task(void *pvParameters);

TaskHandle_t led0task_handler;
void led0_task(void *pvParameters);

TaskHandle_t led1task_handler;
void led1_task(void *pvParameters);

int main(void)
{
    NVIC_PriorityGroupConfig(NVIC_PriorityGroup_4);//设置中断优先级分组4
    delay_init();
    uart_init(115200);
    LED_Init();
    xTaskCreate(start_task,"start task",128,NULL,1,&starttask_handler);
    vTaskStartScheduler();
}
void start_task(void *pvParameters)
{
    taskENTER_CRITICAL();
    xTaskCreate(led0_task,"led0 task",50,NULL,2,&led0task_handler);
    xTaskCreate(led1_task,"led1 task",50,NULL,3,&led1task_handler);
    vTaskDelete(starttask_handler);
    taskEXIT_CRITICAL();            
}

void led0_task(void *pvParameters)
{
    uint8_t num=0;
    while(1)
    {
        num++;
        LED0=~LED0;
        vTaskDelay(1000);
        if (num==5)
        {
            vTaskDelete(led1task_handler);
        }
        
    }
}
void led1_task(void *pvParameters)
{
    while (1)
    {
        LED1=~LED1;
        vTaskDelay(1000);
    }
    
}

